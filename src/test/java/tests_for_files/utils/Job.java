package tests_for_files.utils;

public class Job {
    private String role;
    public String city;
    public int salary;

    public String getRole() {
        return role;
    }
    public String getCity() {
        return city;
    }
    public int getSalary() {
        return salary;
    }

    public void setRole(String role) {
        this.role = role;
    }
    public void setCity(String city) {
        this.city = city;
    }
    public void setSalary(int salary) {
        this.salary = salary;
    }
}
